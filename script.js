function karaokeToLao(text) {
    let lowerText = text.toLowerCase();
    let keys = Object.keys(fullKaraokeToLao).sort((a, b) => b.length - a.length);
    let result = '';
    let i = 0;

    while (i < lowerText.length) {
        let matched = false;

        for (let key of keys) {
            if (lowerText.slice(i, i + key.length) === key) {
                result += fullKaraokeToLao[key] + ' ';
                i += key.length;
                matched = true;
                break;
            }
        }

        if (!matched) {
            result += lowerText[i];
            i++;
        }
    }

    return result.trim();
}

function translateText() {
    const inputText = document.getElementById('inputText').value.trim();
    const outputDiv = document.getElementById('output');
    const loading = document.getElementById('loading');
    const copyBtn = document.getElementById('copyBtn');

    if (!inputText) return;

    loading.style.display = 'block';
    outputDiv.innerHTML = '';
    copyBtn.style.display = 'none';

    setTimeout(() => {
        const result = karaokeToLao(inputText);

        outputDiv.innerText = result;
        loading.style.display = 'none';
        copyBtn.style.display = 'block';
    }, 200);
}

function clearText() {
    document.getElementById('inputText').value = '';
    document.getElementById('output').innerText = '';
    document.getElementById('copyBtn').style.display = 'none';
}

function copyOutput() {
    const outputText = document.getElementById("output").textContent;
    navigator.clipboard.writeText(outputText)
        .then(() => {
            const copyBtn = document.getElementById("copyBtn");
            if (copyBtn) {
                const originalText = copyBtn.textContent;
                copyBtn.textContent = "ຄັດລອກແລ້ວ!";
                setTimeout(() => {
                    copyBtn.textContent = originalText;
                }, 2000);
            }
        })
        .catch(err => {
            console.error("Failed to copy text:", err);
        });
}

